// STL namespaces
using namespace std;

////////////////////////////////////////////////////////////////////////////////
// CLASS DEFINITION
////////////////////////////////////////////////////////////////////////////////

class SqList
{
public:

    //////////////////////////////////////////////////////////////////////////// 
    // PUBLIC TYPE DEFINITIONS
    //////////////////////////////////////////////////////////////////////////// 

    typedef list<list<Int341>> OuterList;

    //////////////////////////////////////////////////////////////////////////// 
    // CONSTRUCTORS/DESTRUCTORS
    //////////////////////////////////////////////////////////////////////////// 

    SqList(void);
    SqList(OuterList);
    SqList(const SqList&);
    ~SqList(void);

    //////////////////////////////////////////////////////////////////////////// 
    // OPERATOR OVERLOADS
    //////////////////////////////////////////////////////////////////////////// 

    const SqList& operator=(const SqList&);
    Int341& operator[](int);

    //////////////////////////////////////////////////////////////////////////// 
    // PUBLIC METHODS
    //////////////////////////////////////////////////////////////////////////// 

    void inspector(void);

    void consolidate(void);

    void addFirst(const Int341&);

    void addLast(const Int341&);

    void add(int, const Int341&);

    Int341 removeFirst(void);

    Int341 removeLast(void);
    
    Int341 remove(int);

    int indexOf(const Int341&);

    int numItems(void);

    void dump(void);

    //////////////////////////////////////////////////////////////////////////// 
    // GETTERS & SETTERS
    //////////////////////////////////////////////////////////////////////////// 

    OuterList getList(void) const { return L; }

private:

    //////////////////////////////////////////////////////////////////////////// 
    // PRIVATE DATA MEMBERS
    //////////////////////////////////////////////////////////////////////////// 

    OuterList L;
    
};

